	import wPicker from "@/components/w-picker/w-picker.vue";
    import xiaEditor from '@/components/xia-editor/xia-editor';
	export default {
		data() {
			return {
				cross:'',
				ruleForm: {
				rukubianhao: this.getUUID(),
				shangpinbianhao: '',
				shangpinmingcheng: '',
				shangpintupian: '',
				kucun: '',
				rukudanjia: '',
				rukuzongjia: '',
				rukubeizhu: '',
				rukuriqi: '',
				yuangonggonghao: '',
				yuangongxingming: '',
				},
				// 登陆用户信息
				user: {},
                ro:{
                   rukubianhao : false,
                   shangpinbianhao : false,
                   shangpinmingcheng : false,
                   shangpintupian : false,
                   kucun : false,
                   rukudanjia : false,
                   rukuzongjia : false,
                   rukubeizhu : false,
                   rukuriqi : false,
                   yuangonggonghao : false,
                   yuangongxingming : false,
                },
			}
		},
		components: {
			wPicker,
            xiaEditor
		},
		computed: {
			baseUrl() {
				return this.$base.url;
			},


			rukuzongjia:{
			get: function () {
				return 1*this.ruleForm.kucun*this.ruleForm.rukudanjia
			}
			},

		},
		async onLoad(options) {
            this.ruleForm.rukuriqi = this.$utils.getCurDateTime();

			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
			// ss读取
			this.ruleForm.yuangonggonghao = this.user.yuangonggonghao
			this.ro.yuangonggonghao = true;
			this.ruleForm.yuangongxingming = this.user.yuangongxingming
			this.ro.yuangongxingming = true;



			// 如果有登陆，获取登陆后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`shangpinruku`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			this.cross = options.cross;
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='rukubianhao'){
					this.ruleForm.rukubianhao = obj[o];
					this.ro.rukubianhao = true;
					continue;
					}
					if(o=='shangpinbianhao'){
					this.ruleForm.shangpinbianhao = obj[o];
					this.ro.shangpinbianhao = true;
					continue;
					}
					if(o=='shangpinmingcheng'){
					this.ruleForm.shangpinmingcheng = obj[o];
					this.ro.shangpinmingcheng = true;
					continue;
					}
					if(o=='shangpintupian'){
					this.ruleForm.shangpintupian = obj[o].split(",")[0];
					this.ro.shangpintupian = true;
					continue;
					}
					if(o=='kucun'){
					this.ruleForm.kucun = obj[o];
					this.ro.kucun = true;
					continue;
					}
					if(o=='rukudanjia'){
					this.ruleForm.rukudanjia = obj[o];
					this.ro.rukudanjia = true;
					continue;
					}
